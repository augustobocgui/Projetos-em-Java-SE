    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Clicou em concluir
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Valor é de preenchimento obrigatório!");
        } else {
            NowString();
            int CodigoAbertura = 0;
            Caixa Setando = new Caixa();
            Setando.setData(s);
            Setando.setHora(f);
            Setando.setOperacao("Abertura");

            try {
                CaixaDAO Inserindo = new CaixaDAO();
                Inserindo.OperandoCaixa(Setando); // Inserindo abertura no banco!
                ResultSet PegandoCodigo = Inserindo.VerificaAbertura(s, f);
                while (PegandoCodigo.next()) {
                    CodigoAbertura = PegandoCodigo.getInt("Codigo");
                }
                PegandoCodigo.close();

                Float ValorInicial = Float.parseFloat(jTextField1.getText().replace(".", "").replace(",", "."));
                Inserindo.ValorInicial(s, ValorInicial, CodigoAbertura, f);


                JOptionPane.showMessageDialog(this, "Abertura de caixa realizada com sucesso!");
            } catch (BancoException ex) {
                Logger.getLogger(AberturaCaixa.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AberturaCaixa.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(CaixaEntrada.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                try {
                    Main.Caixa.AoIniciar();
                } catch (BancoException ex) {
                    Logger.getLogger(AberturaCaixa.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(AberturaCaixa.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (ParseException ex) {
                Logger.getLogger(AberturaCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }

            Main.Caixa.setVisible(true);
            this.dispose();
        }
}//GEN-LAST:event_jButton6ActionPerformed

